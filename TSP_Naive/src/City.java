
public class City {
	
	private double xValue;
	private double yValue;
	
	public City() {
		
	}
	
	public City(double x, double y) {
		xValue = x;
		yValue = y;
	}

	public double getxValue() {
		return xValue;
	}

	public void setxValue(double xValue) {
		this.xValue = xValue;
	}

	public double getyValue() {
		return yValue;
	}

	public void setyValue(double yValue) {
		this.yValue = yValue;
	}
	
}
